// Helper: derive a short monogram + brand color from company name.
function companyBrand(name) {
  const clean = (name || '').replace(/^CÔNG TY\s+\w+\s+/, '').trim();
  const initials = (clean.match(/[A-ZÀ-Ỹ]{2,}/g)?.[0] ?? clean.slice(0, 2) ?? 'CO')
    .slice(0, 3)
    .toUpperCase();
  const palettes = [
    'bg-orange-50 text-orange-600',
    'bg-pink-50 text-pink-600',
    'bg-amber-50 text-amber-600',
    'bg-red-50 text-red-600',
    'bg-sky-50 text-sky-600',
    'bg-green-50 text-green-600',
    'bg-violet-50 text-violet-600',
    'bg-teal-50 text-teal-600',
    'bg-indigo-50 text-indigo-600',
    'bg-rose-50 text-rose-600',
  ];
  let h = 0;
  for (let i = 0; i < name.length; i++) h = (h * 31 + name.charCodeAt(i)) >>> 0;
  return { name: name, initials, brand: palettes[h % palettes.length] };
}

