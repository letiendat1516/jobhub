/**
 * Build a normalized job object from a raw seed entry.
 * (Mirrors what GET /api/jobs will return.)
 */
export function normalizeJob(raw) {
  const salary = raw.salary_text || 'Thoả thuận';
  return {
    id: raw.source_job_id,
    title: raw.job_title,
    company: companyBrand(raw.company_name),
    category: raw.category_name,
    salaryLabel: salary,
    salaryMin: raw._salary_min ?? null,
    salaryMax: raw._salary_max ?? null,
    negotiable: raw._is_negotiable ?? false,
    location:
      raw.location_text
        ?.split('&')[0]
        .replace(/\s*\(mới\)\s*/g, '')
        .trim() ?? '',
    experienceLabel: EXP_LABEL[raw._experience_enum] ?? 'Không yêu cầu',
    experienceEnum: raw._experience_enum,
    employmentType: JOB_TYPE_LABEL[raw._job_type] ?? 'Toàn thời gian',
    jobTypeEnum: raw._job_type,
    workType: WORK_MODE_LABEL[raw._work_mode] ?? 'Tại văn phòng',
    workModeEnum: raw._work_mode,
    tags: raw.experience_tags ?? [],
    postedAgo: (raw.posted_text || '').replace(/^Đăng\s+/, ''),
    postedText: raw.posted_text || '',
    detail: raw.job_detail ?? null,
    hot: (raw._salary_max ?? 0) >= 50000000, // mark high-salary jobs as "hot"
  };
}

